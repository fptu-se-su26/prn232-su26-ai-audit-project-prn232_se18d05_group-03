SELECT
    d.Name                                     AS Khoa,
    COUNT(b.Id)                                AS [Tổng giường],
    SUM(CASE b.Status WHEN 0 THEN 1 ELSE 0 END) AS [Trống],
    SUM(CASE b.Status WHEN 1 THEN 1 ELSE 0 END) AS [Có bệnh nhân],
    SUM(CASE b.Status WHEN 2 THEN 1 ELSE 0 END) AS [Đang dọn],
    SUM(CASE b.Status WHEN 3 THEN 1 ELSE 0 END) AS [Hỏng]
FROM Departments d
LEFT JOIN Beds b ON b.DepartmentId = d.Id
GROUP BY d.Name
ORDER BY d.Name;

